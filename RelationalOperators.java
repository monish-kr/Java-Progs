package fundamentals;

public class RelationalOperators {

	public static void main(String args[])
	{
		int a=10, b=20;
		System.out.println(a>b); //greater than
		System.out.println(a<b); //smaller than
		System.out.println(a==b); //comparison (equal-to)
		System.out.println(a!=b); //comparison (not equal-to)
		System.out.println(a>=b); //greater than equal to
		System.out.println(a<=b); //smaller than equal to
		
	}
	
}
